  quantity_base_units numeric(12,3) not null check (quantity_base_units > 0),
  package_id bigint references public.product_packages (id) on delete set null,
  package_count numeric(12,3),
  unit_price numeric(12,2),
  reference_type text,
  reference_id text,
  note text,
  created_by uuid references public.employees (id) on delete set null,
  created_at timestamp with time zone not null default now()
);

create index if not exists stock_movements_v13_product_idx
