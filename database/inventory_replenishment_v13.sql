create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  sku text unique,
  ean text,
  product_category text not null
    check (product_category in (
      'beverage_ready',
      'snack_ready',
      'food_ready',
      'ingredient',
      'consumable',
      'service_material',
      'spare_part'
    )),
  usage_type text not null
    check (usage_type in (
      'direct_sale',
      'recipe_consumption',
      'internal_consumption',
      'service_use'
    )),
  base_unit text not null default 'ks',
  vat_rate numeric(5,2),
  purchase_price numeric(12,2),
  sale_price numeric(12,2),
  shelf_life_days integer,
  expiry_tracking_mode text not null default 'none'
    check (expiry_tracking_mode in ('none', 'best_before', 'use_by')),
  expiry_warning_days integer not null default 0,
  requires_batch_tracking boolean not null default false,
  active boolean not null default true,
  note text,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

create index if not exists products_category_idx
  on public.products (product_category);

create index if not exists products_usage_type_idx
  on public.products (usage_type);

create index if not exists products_active_idx
  on public.products (active);

create table if not exists public.product_packages (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.products (id) on delete cascade,
  package_name text not null,
  units_per_package numeric(12,3) not null check (units_per_package > 0),
  package_barcode text,
  is_default boolean not null default false,
  active boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  unique (product_id, package_name)
);

create index if not exists product_packages_product_idx
  on public.product_packages (product_id);

