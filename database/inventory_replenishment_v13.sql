create index if not exists stock_movements_v13_from_idx
  on public.stock_movements_v13 (from_stock_location_id);

create index if not exists stock_movements_v13_to_idx
  on public.stock_movements_v13 (to_stock_location_id);

create index if not exists stock_movements_v13_batch_idx
  on public.stock_movements_v13 (batch_id);

