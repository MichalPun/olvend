create table if not exists public.technical_job_attachments (
  id bigint generated by default as identity primary key,
  technical_job_id bigint not null references public.technical_jobs (id) on delete cascade,
  attachment_type text not null default 'photo',
  attachment_url text not null,
  note text,
  created_by uuid references public.employees (id) on delete set null,
  created_at timestamp with time zone not null default now()
);

create or replace function public.set_technical_jobs_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  if new.status = 'workshop_ready' and new.workshop_ready_at is null then
    new.workshop_ready_at = now();
  end if;
  if new.status = 'pending_confirmation' and new.submitted_at is null then
    new.submitted_at = now();
  end if;
  if new.status = 'closed' and new.finished_at is null then
    new.finished_at = now();
  end if;
  return new;
end;
$$;

drop trigger if exists trg_technical_jobs_updated_at on public.technical_jobs;

create trigger trg_technical_jobs_updated_at
before update on public.technical_jobs
for each row
execute function public.set_technical_jobs_updated_at();

create or replace function public.set_technical_job_checklist_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  if new.completed and new.completed_at is null then
    new.completed_at = now();
  end if;
  if not new.completed then
    new.completed_at = null;
  end if;
  return new;
end;
$$;

drop trigger if exists trg_technical_job_checklist_updated_at on public.technical_job_checklist_items;

create trigger trg_technical_job_checklist_updated_at
before update on public.technical_job_checklist_items
for each row
execute function public.set_technical_job_checklist_updated_at();

