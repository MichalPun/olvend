create table if not exists public.technical_jobs (
  id bigint generated by default as identity primary key,
  job_type text not null
    check (job_type in ('service', 'installation', 'deinstallation', 'transfer', 'revision')),
  status text not null default 'new'
    check (status in ('new', 'planned', 'assigned', 'workshop_ready', 'on_the_way', 'on_site', 'in_progress', 'pending_confirmation', 'closed', 'blocked', 'postponed', 'cancelled')),
  priority text not null default 'normal'
    check (priority in ('low', 'normal', 'high', 'critical')),
  title text not null,
  description text,
  note text,
  result text,
  location_id bigint references public.locations (id) on delete set null,
  machine_id bigint references public.machines (id) on delete set null,
  source_location_id bigint references public.locations (id) on delete set null,
  target_location_id bigint references public.locations (id) on delete set null,
  assigned_employee_id uuid references public.employees (id) on delete set null,
  workshop_ready boolean not null default false,
  workshop_ready_at timestamp with time zone,
  workshop_ready_by uuid references public.employees (id) on delete set null,
  workshop_note text,
  configuration_kind text not null default 'general'
    check (configuration_kind in ('general', 'food', 'coffee')),
  configuration_payload jsonb not null default '{}'::jsonb,
  planned_date date,
  due_at timestamp with time zone,
  arrived_at timestamp with time zone,
  started_at timestamp with time zone,
  finished_at timestamp with time zone,
  submitted_at timestamp with time zone,
  manager_confirmed_at timestamp with time zone,
  manager_confirmed_by uuid references public.employees (id) on delete set null,
  qr_token text not null unique,
  protocol_url text,
  active boolean not null default true,
  created_by uuid references public.employees (id) on delete set null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

