create table if not exists public.technical_job_protocols (
  id bigint generated by default as identity primary key,
  technical_job_id bigint not null references public.technical_jobs (id) on delete cascade,
  protocol_kind text not null default 'standard',
  protocol_html text,
  generated_at timestamp with time zone not null default now(),
  generated_by uuid references public.employees (id) on delete set null,
  unique (technical_job_id)
