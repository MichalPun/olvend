insert into public.business_contacts (
  contact_type,
  name,
  name_norm,
  company_id,
  tax_id,
  contact_name,
  email,
  phone,
  billing_address,
  default_due_days,
  note,
  active,
  source,
  purchase_supplier_id
)
select
  'supplier',
  ps.name,
  public.olvend_contact_norm(ps.name),
  ps.company_id,
  ps.tax_id,
  ps.contact_name,
  ps.email,
  ps.phone,
  ps.address,
  ps.default_due_days,
  ps.note,
  ps.active,
  'purchase_suppliers',
  ps.id
from public.purchase_suppliers ps
where trim(coalesce(ps.name, '')) <> ''
on conflict (contact_type, name_norm)
do update set
  name = excluded.name,
  company_id = coalesce(excluded.company_id, public.business_contacts.company_id),
  tax_id = coalesce(excluded.tax_id, public.business_contacts.tax_id),
  contact_name = coalesce(excluded.contact_name, public.business_contacts.contact_name),
  email = coalesce(excluded.email, public.business_contacts.email),
  phone = coalesce(excluded.phone, public.business_contacts.phone),
  billing_address = coalesce(excluded.billing_address, public.business_contacts.billing_address),
  default_due_days = coalesce(excluded.default_due_days, public.business_contacts.default_due_days),
  note = coalesce(excluded.note, public.business_contacts.note),
  active = excluded.active,
  source = excluded.source,
  purchase_supplier_id = excluded.purchase_supplier_id;

insert into public.business_contacts (
  contact_type,
  name,
  name_norm,
  active,
  source
)
select
  'customer',
  trim(sd.customer_name),
  public.olvend_contact_norm(sd.customer_name),
  true,
  'sales_documents'
from public.sales_documents sd
where trim(coalesce(sd.customer_name, '')) <> ''
group by trim(sd.customer_name), public.olvend_contact_norm(sd.customer_name)
on conflict (contact_type, name_norm)
do update set
  name = excluded.name,
  active = true;
