drop policy if exists "Allow write control campaigns" on public.control_campaigns;
create policy "Allow write control campaigns" on public.control_campaigns
for all to authenticated, anon using (true) with check (true);

drop policy if exists "Allow read control campaign locations" on public.control_campaign_locations;
create policy "Allow read control campaign locations" on public.control_campaign_locations
for select to authenticated, anon using (true);

drop policy if exists "Allow write control campaign locations" on public.control_campaign_locations;
create policy "Allow write control campaign locations" on public.control_campaign_locations
for all to authenticated, anon using (true) with check (true);

drop policy if exists "Allow read control visits" on public.control_visits;
create policy "Allow read control visits" on public.control_visits
for select to authenticated, anon using (true);

drop policy if exists "Allow write control visits" on public.control_visits;
create policy "Allow write control visits" on public.control_visits
